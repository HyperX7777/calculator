// Addition

export function sum(a:number, b:number) {
    return a + b
};

// Subtraction

export function difference(a:number, b:number) {
    return a - b
};

// Division

export function divsion(a:number, b:number) {
    return a / b
};

// Multiplication

export function product(a:number, b:number) {
    return a * b
};